int query(int node, int b, int e, int i, int j);
void update(int node, int b, int e, int i, int newvalue);


int arr[MAX], tree[4*MAX];

void init(int node, int b, int e) // initializations
{
    if(b == e)
    {
        tree[node] = arr[b];
        return;
    }
    int left = 2*node;
    int right = left+1;
    int mid = (b+e)/2;
    init(left, b, mid);
    init(right, mid+1, e);
    tree[node] = tree[left]+tree[right];
}

int query(int node, int b, int e, int i, int j) // question
{
    if(i > e || j < b)
        return 0;
    if(b >= i && e <= j)
        return tree[node];
    int left = 2*node;
    int right = left+1;
    int mid = (b+e)/2;
    int ret1 = query(left, b, mid, i, j);
    int ret2 = query(right, mid+1, e, i, j);
    return ret1+ret2;
}

void update(int node, int b, int e, int i, int newvalue) // update
{
    if(i > e || i < b)
        return;
    if(b >= i && e <= i)
    {
        tree[node] = newvalue;
        return;
    }
    int left = 2*node;
    int right = left+1;
    int mid = (b+e)/2;
    update(left, b, mid, i, newvalue);
    update(right, mid+1, e, i, newvalue);
    tree[node] = tree[left] + tree[right];
}


