vector<int> adj[MAX]; // adjacency list of graph
int n; // number of nodes
vector<bool> visited;
vector<int> tin, low;
int timer;

void ISBRIDGE(int v, int to);

int dfs(int v, int p = -1) {
    visited[v] = true;
    tin[v] = low[v] = timer++;
    int ret = 1;
    for (int to : adj[v]) {
        if (to == p) {
            continue;
        }
        if (visited[to]) {
            low[v] = min(low[v], tin[to]);
        } else {
            int val = dfs(to, v);
            ret += val;
            low[v] = min(low[v], low[to]);
            if (low[to] > tin[v])
                ISBRIDGE(v, to);
        }
    }
    return ret;
}

void find_bridges() {
    timer = 0;
    visited.assign(n+1, false);
    tin.assign(n+1, -1);
    low.assign(n+1, -1);
    for (int i = 1; i <= n; ++i) {
        if (!visited[i])
            dfs(i);
    }
}


