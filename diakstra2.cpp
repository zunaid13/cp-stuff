const int INF = 1e17;

int dist[MAX];
vector <pii> adj[MAX];
int prev_node[MAX];
int n, m;

void diakstra(int src)
{
    for(int i = 1 ; i <= n ; i++)
    {
        dist[i] = INF;
        prev_node[i] = 0;
    }
    dist[src] = 0;
    priority_queue <pii, vector<pii>, greater <pii>> pq;
    pq.push({dist[src], src});
    while(pq.size())
    {
        auto [d, v] = pq.top();
        pq.pop();
        if(dist[v] != d) continue;
        for(auto [x,y] : adj[v])
        {
            if(d + y < dist[x])
            {
                dist[x] = d + y;
                prev_node[x] = v;
                pq.push({dist[x], x});
            }
        }
    }
}

int solve(int testcase)
{
    cin >> n >> m;
    for(int i = 0 ; i < m ; i++)
    {
        int x, y, w;
        cin >> x >> y >> w;
        adj[x].push_back({y, w});
        adj[y].push_back({x, w});
    }
    diakstra(1);
    if(dist[n] == INF)
    {
        cout << -1 << endl;
        return 0;
    }
    vector <int> res;
    int curr = n;
    while(curr != 1)
    {
        res.push_back(curr);
        curr = prev_node[curr];
    }
    res.push_back(curr);
    reverse(all(res));
    for(auto x : res) cout << x << " ";
    cout << endl;   
    return 0;
}

